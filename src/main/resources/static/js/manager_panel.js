function displayAccount(accountID, username, firstName, lastName, accountStatus, address, email, homeNumber, cellPhoneNumber) {
    document.getElementById("accountID").value = accountID;
    document.getElementById("username").value = username;
    document.getElementById("firstName").value = firstName;
    document.getElementById("lastName").value = lastName;
    document.getElementById("accountStatus").value = accountStatus;
    document.getElementById("address").value = address;
    document.getElementById("email").value = email;
    document.getElementById("homeNumber").value = homeNumber;
    document.getElementById("cellPhoneNumber").value = cellPhoneNumber;
}

